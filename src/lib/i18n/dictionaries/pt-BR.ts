// Deep type that converts all literal strings to `string` for i18n compatibility
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly string[]
      ? string[]
      : T[K] extends readonly { value: string; label: string }[]
        ? { value: string; label: string }[]
        : T[K] extends object
          ? DeepStringify<T[K]>
          : T[K];
};

